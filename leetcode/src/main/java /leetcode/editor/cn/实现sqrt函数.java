class Solution {
    public int mySqrt(int x) {
        if (x == 0 || x == 1) {
            return x;
        }
        int start = 1;
        int end = x;
        int res = 0;
        while (start <= end) {
            int mid = (start + end) / 2;
            if (mid == x / mid) {
                return mid;
            } else if (mid > x / mid) {
                end = mid - 1;
            } else {
                start = mid + 1;
                //ä¸èƒ½æ•´é™¤ å–ä¸­é—´å€¼
                res = mid;
            }
        }
        return res;
    }
}
