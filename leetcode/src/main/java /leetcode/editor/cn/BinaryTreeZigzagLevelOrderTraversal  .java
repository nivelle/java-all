class Solution {
    public List<List<Integer>> zigzagLevelOrder(TreeNode root) {
        List<List<Integer>> res = new ArrayList<>();
        if (root == null) return res;
        LinkedList<TreeNode> queue = new LinkedList<>();
        queue.add(root);
        int level = 1;
        while (!queue.isEmpty()) {
            int len = queue.size();
            ArrayList<Integer> list = new ArrayList<>();
            for (int i = 0; i < len; i++) {
                TreeNode node = queue.pop();
                //å¥‡æ•°å±‚ä»å·¦å¾€å³ï¼Œå¶æ•°å±‚ä»å³å¾€å·¦
                if (level % 2 == 1) {
                    list.add(node.val);
                } else {
                    list.add(0, node.val);
                }
                //ç›¸åŒå±‚åºçš„èŠ‚ç‚¹å½’å…¥åŒä¸€ä¸ªæ•°ç»„
                if (node.left != null) {
                    queue.add(node.left);
                }
                if (node.right != null) {
                    queue.add(node.right);
                }
            }
            res.add(list);
            //å±‚æ•°å¢åŠ 
            level++;
        }
        return res;
    }
}
